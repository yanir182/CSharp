        {
            TcpListener server = new TcpListener(IPAddress.Any, 5000);
            server.Start();
            Console.WriteLine("САМЫЙ ВЕЛИКИЙ И ПОЛНЫЙ СБОРНИК ЦИТАТ ДЖЕЙСАНА СТЕТХЕМА P.s именно так и появилась свагушка на Русси.......");

            Thread gg = new Thread(Chitat_Legendy);
            gg.Start();

            while (true)
            {
                TcpClient client = server.AcceptTcpClient();
                clients.Add(client);

                Console.WriteLine("Новый кент подключён.");
                BroadcastsystemMessage("В наш клуб зашел новый философ.");
            }
        }

        static void Chitat_Legendy()
        {
            int i = 0; 
            while (true)
            {
                if (i< chitat.Length) {
                    if (clients.Count > 0)
                    {
                        string quote = chitat[i];
                        BroadcastsystemMessage(quote);

                        i += 1;
                    }
                    Thread.Sleep(3);
                }
                else
                {
                    i = 0;
                }
            }
        }

        static void BroadcastsystemMessage(string massage)
        {
            string fm = "[Пацанская Цитата]: " + massage;
            byte[] data = Encoding.UTF8.GetBytes(fm);

            lock (clients)
            {
                for (int i = clients.Count - 1; i >= 0; i--)
                {
                    try
                    {
                        NetworkStream stream = clients[i].GetStream();
                        stream.Write(data, 0, data.Length);
                    }
                    catch
                    {
                        clients[i].Close();
                        clients.RemoveAt(i);
                    }
                }
            }
            Console.WriteLine(fm);
        }
